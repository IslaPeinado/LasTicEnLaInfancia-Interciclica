    <h2>ACTIVIDADES PARA REALIZAR </h2>
    <p>Cuando los niños todavía son bebés es más difícil que ellos mismos utilicen directamente la tecnología, ya que todavía no son conscientes y les puede resultar difícil su manejo. Lo que podemos hacer es utilizar nosotros algún recurso, de una manera en la que el niño esté involucrado. Por ejemplo durante la edad de 0 a 1 año una buena actividad sería coger una cámara de fotos y fotografiar distintas partes del cuerpo del bebé, como la mano, las orejas, el pie, la boca, los ojos, etc. Las llevaremos a imprimir y trabajaremos con ellas, el bebe podrá verlas y cogerlas, de esta manera. Durante la edad de 1 a 2 años podemos realizar otra actividad relacionada con la anterior, que consiste en mostrar en un ordenador las mismas partes del cuerpo que en la edad anterior y cuando identifiquen que parte es les pondremos una canción sobre la función de esa parte del cuerpo. Ejemplo: nariz para oler, ojos para ver,...
      <br>
      A partir de los 2 años de edad comienza la edad más adecuada para introducir la tecnología a los niños por lo que es más fácil encontrar actividades y juegos para realizar con los niños. Es importante, por ejemplo, que cuando empiecen a coger un ordenador les enseñemos el manejo del ratón, o en las pizarras digitales, cómo utilizar el bolígrafo. Podemos encontrar gran variedad de aplicaciones educativas: 
    </p>
    <ul>
      <li> <u><i>Leo con Grin:</i></u> es una buena forma de iniciar la lectura. Consiste en diferentes juegos con dos niveles de dificultad, que enseñan a silabear, y leer y escribir palabras y frases. </li>
      <li> <u><i>Encuéntralos a todos:</i></u> Dinosaurios: los dinosaurios es un tema que les suele gustar mucho a los niños. Esta aplicación muestra hasta 24 dinosaurios que deben encontrar en la pantalla, y nos ofrece una información bastante detallada y comprensible para los más pequeños. Además se pueden realizar puzzles y tomar fotos.</li>
      <li> <u><i>Sago Mini Space Explorer:</i></u> esta aplicación es para niños un poco más mayores, ya que su objetivo es conocer el espacio y descubrir el planeta. </li>
      <li> <u><i>Aprendo Música:</i></u> es una página web para iniciarse con la música, aprendiendo notas musicales, el ritmo o conocer los instrumentos.</li>
    </ul>
