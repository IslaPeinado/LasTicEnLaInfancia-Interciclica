    <h2>¿CÓMO UTILIZARLAS DE MANERA CORRECTA?</h2>
    <p>La idea de utilizar la tecnología para la educación es complementarla, tiene una función de apoyo.  A esta edad lo ideal es facilitar a los niños contenidos que les hagan interactuar (canciones, videos, juegos educativos…). Debemos evitar poner una pantalla ante los niños como entretenimiento o para que se mantengan calmados, puesto que de esta manera estaremos dejando de lado el juego tradicional apartado y estamos utilizando las TIC por comodidad propia, y no con un fin lúdico. 
      <br><br>
      Por otra parte, podemos utilizarlas fuera del aula, en casa. Estos son algunos consejos a tener en cuenta para hacer un uso responsable con nuestros hijos:
      <br>
      Los pediatras recomiendan que a partir de los 2 años los niños utilicen la tecnología un máximo de dos horas diarias.
      <br>
      Establecer normas sobre el tiempo de televisión, por ejemplo, no encenderla durante las comidas o verla solo un rato por la tarde.
      <br>
      Si el niño tiene claras las normas generará una rutina evitando un uso excesivo de la pantalla. 
      <br>
      También es muy importante no utilizar el sonido de la tele para ambientar, es decir, para escuchar algo de fondo, ya que de esta forma estará constantemente encendida y no favorecerá que el niño aprenda las normas. 
      <br>
      Los padres deben controlar el contenido de cualquier tipo de dispositivo que el niño pueda tener a su alcance. De esta forma los adultos seleccionarán las preferencias para sus hijos y estarán seguros de los sitios a los que pueden acceder. 
      <br>
      Los niños tienden a imitar a los adultos en todo lo que hacen, es parte de su desarrollo, por lo tanto, si observa que los padres pasan mucho tiempo ante cualquier tipo de dispositivo (móvil, televisión, tablet…) el pequeño querrá hacer lo mismo. Es responsabilidad del adulto hacer uso responsable de la tecnología. 
      <br>
      Es importante utilizar la tecnología junto con los niños, dialogando, jugando juntos, etc. puesto que esto también es un proceso para el niño hasta que este pueda utilizarlo por sí mismo.
    </p>
    
