      <li>Hay un gran número de <strong> programas y aplicaciones </strong> disponibles adaptados a las necesidades de cada niño que nos dan la posibilidad de crear contenido. Además las aplicaciones están planeadas cada una para necesidades concretas, permitiendo una enseñanza individualizada y personalizada. </li>
