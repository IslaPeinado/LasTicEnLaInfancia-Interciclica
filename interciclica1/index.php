    <h2>¿QUÉ TICS USAR EN INFANTIL?</h2>
    <p>La manera más fácil de trabajar las nuevas tecnologías con los niños es a través de blogs, aplicaciones o videos, por lo que los recursos más utilizados, y que cada familia suele tener en su casa son los ordenadores, móviles, o tablets; pero aquí podemos conocer todas las opciones para trabajar con los pequeños:</p>
    <ul>
      <li> <strong>Ordenadores:</strong> como ya hemos mencionado, es el más recurrido como método de aprendizaje, tanto en casa como en el aula. Lo podemos utilizar para buscar información, ver videos e imágenes, realizar actividades, etc. </li>
      <li> <strong>Pizarras digitales:</strong> este recurso es más empleado en las aulas. Normalmente hay una sala de audiovisuales específica para que el educador pueda bajar con sus alumnos a utilizarla. Los niños pueden jugar con los bolígrafos interactivos, escribiendo, dibujando y uniendo elementos de una forma diferente a la que suelen estar más acostumbrados, que es el ratón tradicional, algo que les suele costar un poco más a los niños pequeños. </li>
      <li> <strong>Videocámaras y cámaras de fotos:</strong> aunque no lo creamos tienen mucho potencial en la etapa de  Infantil ya que permiten una participación activa de los niños. Una de las mejores maneras de que los niños aprendan es a través de la vista, y qué mejor manera de hacerlo que tomando fotos de lo que queremos enseñarles. Incluso, si el niño es más mayor y tiene la habilidad suficiente, podrá tomar el mismo la fotografía, algo que le resultará bastante  divertido.</li>
      <li> <strong>Proyector de video:</strong> es parecido a la pizarra digital, lo único que no es tan interactiva. El proyector nos permite ver imágenes a un tamaño mayor que una simple pantalla. Es una buena forma de trabajar en clase de manera conjunta, con todos los niños a la vez</li>
      <li> <strong>Herramientas de video chat:</strong> es algo que les llama la atención a los niños, el hecho de ver algo o a alguien conocido a través de una pantalla. Podemos conectarnos con algún primo, con la abuela, etc. En ocasiones podemos conectarnos de manera grupal con el profesor de la escuela o con otros centros escolares para que vean como son otros colegios, otros patios,...</li>
    </ul>
